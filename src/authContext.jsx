import React, { useReducer } from "react";
import MkdSDK from "./utils/MkdSDK";

export const AuthContext = React.createContext();

const initialState = {
  isAuthenticated: false,
  user: null,
  token: null,
  role: null,
};

const reducer = (state, action) => {
  switch (action.type) {
    case "LOGIN":
      //TODO
      //STORING THE TOKEN GOTTEN AFTER LOGGING IN TO LOCAL STORAGE
      localStorage.setItem("token", action.data.token);
      return {
        ...state,
      };
    case "LOGOUT":
      localStorage.clear();
      return {
        ...state,
        isAuthenticated: false,
        user: null,
      };
    default:
      return state;
  }
};

let sdk = new MkdSDK();

export const tokenExpireError = (dispatch, errorMessage) => {
  const role = localStorage.getItem("role");
  if (errorMessage === "TOKEN_EXPIRED") {
    dispatch({
      type: "Logout",
    });
    window.location.href = "/" + role + "/login";
  }
};

const AuthProvider = ({ children }) => {
  const [state, dispatch] = useReducer(reducer, initialState);

  //IMPLEMENTING THE FUNCTION TO CHECK IF THE TOKEN IS STILL VALID
  const checkToken = () => {
    sdk
      .check({
        role: "admin",
      })
      .then((response) => console.log("Token Validation Output:", response));
  };

  React.useEffect(() => {
    //TODO
    //CALLING IT TO RENDER ON PAGE LOAD
    checkToken();
  }, []);

  return (
    <AuthContext.Provider
      value={{
        state,
        dispatch,
      }}
    >
      {children}
    </AuthContext.Provider>
  );
};

export default AuthProvider;
